//----------------------------------------------------------------------------//
void AbuShumaysQuadrupleRange::build_impl(c_dbl a, c_dbl b)
{
  Insist(d_m  <= 30, "A-S quadruple range is for 30 or fewer points");
  Insist(detran_utilities::soft_equiv(std::cos(a), 1.0) &&
         detran_utilities::soft_equiv(std::cos(b), 0.0),
         "A-S Q-R is defined only for first quadrant azimuths, [0, pi/2].");

  for (int i = 0; i < d_m; ++i)
  {
    // the functions below store in the opposite order enforced by the base
    d_x[d_m-i-1] = std::acos(get_phi(d_m, 0, i));
    d_w[d_m-i-1] = get_phi(d_m, 1, i);
  }
}

